package fr.digicar.model;

public enum UserStatus {

    /**
     * Active user status.
     * Refers to an active user.
     */
    ACTIVE,

    /**
     * Inactive user status.
     * Refers to an inactive user.
     */
    INACTIVE,

    /**
     * Banned user status.
     * Refers to a banned user.
     */
    BANNED

}
